package model;

public class Preference {

	private String id;
	private String topdesc;
	private String middesc;
	private String lowdesc;
	private double weight;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	public String getTopdesc() {
		return topdesc;
	}
	public void setTopdesc(String topdesc) {
		this.topdesc = topdesc;
	}
	public String getMiddesc() {
		return middesc;
	}
	public void setMiddesc(String middesc) {
		this.middesc = middesc;
	}
	public String getLowdesc() {
		return lowdesc;
	}
	public void setLowdesc(String lowdesc) {
		this.lowdesc = lowdesc;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	
	
	
	
	
}
